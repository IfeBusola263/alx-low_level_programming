#include "3-calc.h"

/**
 * op_add - adds numbers
 * @a: formal parameter
 * @b: formal parameter
 *
 * Return: returns an integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts numbers
 * @a: formal parameter
 * @b: formal parameter
 *
 * Return: returns an integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies numbers
 * @a: formal parameter
 * @b: formal parameter
 *
 * Return: returns an integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b
 * @a: formal parameter
 * @b: formal parameter
 *
 * Return: returns an integer
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - gets remainder of number division
 * @a: formal parameter
 * @b: formal parameter
 *
 * Return: returns an integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
